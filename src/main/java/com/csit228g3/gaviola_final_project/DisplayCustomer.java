    public void initData() {
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        tableModel.setRowCount(0);
        try {
            ResultSet rs = dbHelper.getCustomers();
            while (rs.next()) {
                int id = rs.getInt("id");
                String first_name = rs.getString("first_name");
                String last_name = rs.getString("last_name");
                String phone_number = rs.getString("phone_number");
                Object[] row = {id, first_name, last_name, phone_number};
                addRowToTable(row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(DisplayCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void addRowToTable(Object[] dataRows) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        model.addRow(dataRows);
    }

