    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
// TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedRowIndex = jTable1.getSelectedRow();
        if (selectedRowIndex == -1) {
            return;
        }
        int id = Integer.parseInt(model.getValueAt(selectedRowIndex, 0).toString());
        UpdateCustomer updateCustomer = new UpdateCustomer();
        updateCustomer.setTextById(id);
        updateCustomer.setVisible(true);
        dispose();
    }//GEN-LAST:event_btnUpdateMouseClicked

    private void btnGoFilterMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGoFilterMouseClicked
        // TODO add your handling code here:
        String filter = txtFilter.getText();
        Object selectedItem = cbFilter.getSelectedItem();
        try {
            ResultSet rs;
            switch (selectedItem.toString()) {
                case "ID" ->
                    rs = dbHelper.getCustomerById(Integer.parseInt(filter));
                case "First Name" ->
                    rs = dbHelper.getCustomerByFirstName(filter);
                case "Last Name" ->
                    rs = dbHelper.getCustomerByLastName(filter);
                case "Phone Number" ->
                    rs = dbHelper.getCustomerByPhoneNumber(filter);
                default ->
                    rs = dbHelper.getCustomers();
            }
            rsAddRow(rs);
        } catch (SQLException e) {
            Logger.getLogger(DisplayCustomer.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnGoFilterMouseClicked

